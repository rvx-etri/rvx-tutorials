int sensor_write(struct reginfo *reg_info)
{
	printf("sensor_write   reg : %08X\n", reg_info->reg);

	switch(reg_info->reg) {
	case SEQUENCE_WAIT_MS:
		i2c_delay(reg_info->val);
		break;

	case SEQUENCE_WAIT_US:
		i2c_delay(1);
		break;

	case SEQUENCE_PROPERTY:
		break;

	default:
		i2c_write2addr(I2C_CIS, reg_info->reg, reg_info->val, (reg_info->reg_len==BYTE_LEN) ? 1 : 2);
		my_getc();
		break;

	}

	return 1;
}

